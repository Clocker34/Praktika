// Менеджер аутентификации (задание 3 п.1–3, 7–9).
// JWT-токены хранятся только в оперативной памяти (п.9).
// Токены не передаются клиентам через RPC (п.8).
#pragma once

#include <string>

namespace auth {

// Инициализировать менеджер (запуск фонового потока обновления).
void Init();

// Остановить фоновый поток обновления.
void Shutdown();

// Аутентификация: POST /api/v1/auth/login.
// При успехе сохраняет access + refresh в памяти.
// Возвращает 0 при успехе, код ошибки при неудаче.
long Login(const wchar_t* username, const wchar_t* password);

// Выход из аккаунта: удаляет access + refresh (п.3, п.7).
void Logout();

// Проверить, аутентифицирован ли пользователь.
bool IsAuthenticated();

// Получить имя (логин) текущего аутентифицированного пользователя.
// Пустая строка, если не аутентифицирован.
std::wstring GetUsername();

// Получить текущий Access-токен (для внутреннего использования службой).
// Вызывается license_manager для запросов к API.
// НЕ передаётся через RPC (п.8).
std::string GetAccessToken();

}  // namespace auth
