// Менеджер лицензирования (задание 3 п.4–7, 9, 11).
// Лицензионный тикет хранится только в оперативной памяти (п.9).
// Тикет не передаётся клиентам через RPC (п.8).
#pragma once

#include <string>

namespace license {

// Инициализировать менеджер (запуск фонового потока обновления).
void Init();

// Остановить фоновый поток.
void Shutdown();

// Запросить текущий статус лицензии с сервера (п.4).
// Возвращает 0 при успехе (тикет сохранён), код ошибки при неудаче.
long FetchLicenseStatus();

// Активировать продукт по коду (п.6).
// Возвращает 0 при успехе.
long Activate(const wchar_t* activationCode);

// Удалить лицензионный тикет (при выходе из аккаунта, п.7).
void ClearTicket();

// Есть ли действующая лицензия.
bool IsLicensed();

// Дата истечения лицензии (формат "YYYY-MM-DD HH:MM:SS").
// Пустая строка, если лицензии нет.
std::wstring GetExpiryDate();

}  // namespace license
